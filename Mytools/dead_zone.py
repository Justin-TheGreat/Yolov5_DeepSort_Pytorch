import math

def dead_zone(a, target, theta=20):
  x = target[0]
  y = target[1]
  theta_rad = math.radians(theta)
  r = a/math.tan(theta_rad)

  if theta==45:
    r = a/1

  #print(f'r={r}')

#   y_up = math.sqrt(math.pow(r,2) - math.pow((x-r),2)) - a
  y_up = abs((math.pow(r,2) - math.pow((x-r),2))**0.5) - a
  y1 = y_up

  #print(f'y1={y1}')

  distance = abs(y1-y)

  #print(f'distance={distance}')
  return distance
